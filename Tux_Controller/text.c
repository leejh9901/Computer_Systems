/*
 * text_to_graphics
 *   DESCRIPTION: populate the string information to the buffer in the modex form
 *   INPUTS:    unsigned char buf[] - the buffer that will be populated in the modex form
 *              char s[] - the string we want to put on the status bar
 *              int p_off_given - the plane offset of the first pixel in the logic view window
 *   OUTPUTS: none
 *   RETURN VALUE: none
 *   SIDE EFFECTS: copies to the build buffer;
 */
void text_to_graphics(unsigned char buf[], char s[], int p_off_given) {
    int m = 3;                  // counter that mimics plane offset
    static int PLANEOFFSET = 1440;  // 1440 = 320 / 4 * 18
                                    // (SIZE OF A PLANE) = (SCREEN_X_WIDTH) x (HEIGHT of STATUSBAR)
    int w = 0;                      // counter
    
    for(m = 3; m >= 0; m--) {
        for (w = 0; w < 80; w++) {
            buf[m * PLANEOFFSET + w] = 64;          // set the color of the top row of the status bar
            buf[m * PLANEOFFSET + w+1360] = 64;     // set the color of the bottom row of the status bar
                                                    // 1360 = 17 * 80 
                                                    // (The number of pixelsin the status bar
                                                    // until the first pixel in the bottom row 
                                                    // of the status bar)
        }
    }

    int i, j, k;
    for (j = 0; j < 16; j++)  {                         // rows (height of a char)
        for (i = 0; i < 40; i++) {                      // # of chars in the status bar
          unsigned char currChar = font_data[(int)(s[i])][j];
            for (k = 0; k < 8; k++) {                   // cols (width of a char)
                
                // if the digit is 1, set it to color1 (10);
                // if the digit is 0, set it to color2 (20);
                unsigned char result = currChar & (0x80);
                if (result == 0x00) {
                    buf[p_off_given * PLANEOFFSET + w] = 64;
                } else {
                    buf[p_off_given * PLANEOFFSET + w] = 65;
                }
            
                p_off_given--;
                
                if (p_off_given < 0) {
                    p_off_given = 3;
                    w++;
                }
               currChar = currChar << 1;        // shift to left
            }
        }
    }
   
    return;
}


